public class MainActivity extends AppCompatActivity implements BottomNavigationView.OnNavigationItemSelectedListener {

    BottomNavigationView bottomNavigationView;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        bottomNavigationView = findViewById(R.id.bottom_navigation);

        getSupportFragmentManager().beginTransaction().replace(R.id.navigationFragments,new TextFragment()).commit();

        bottomNavigationView.setOnNavigationItemSelectedListener(this);
    }

    @Override
    public boolean onNavigationItemSelected(@NonNull MenuItem item) {
        switch (item.getItemId()){
            case R.id.camera:
                getSupportFragmentManager().beginTransaction().replace(R.id.navigationFragments,new CameraFragment()).commit();
                break;
            case R.id.text:
                getSupportFragmentManager().beginTransaction().replace(R.id.navigationFragments,new TextFragment()).commit();
                break;
        }
        return true;
    }

}
